package com.fh.utilmy;

public class DefineServiceConfig {
	private String serviceKey;
	private String serviceClass;
	private String serviceMethod;
	private String serviceInPara;
	private String serviceOutPara;
	private String authCode;
	
	private String moduleName;
	private String funcName;
	public String getServiceKey() {
		return serviceKey;
	}
	public void setServiceKey(String serviceKey) {
		this.serviceKey = serviceKey;
	}
	public String getServiceClass() {
		return serviceClass;
	}
	public void setServiceClass(String serviceClass) {
		this.serviceClass = serviceClass;
	}
	public String getServiceMethod() {
		return serviceMethod;
	}
	public void setServiceMethod(String serviceMethod) {
		this.serviceMethod = serviceMethod;
	}
	public String getServiceInPara() {
		return serviceInPara;
	}
	public void setServiceInPara(String serviceInPara) {
		this.serviceInPara = serviceInPara;
	}
	public String getServiceOutPara() {
		return serviceOutPara;
	}
	public void setServiceOutPara(String serviceOutPara) {
		this.serviceOutPara = serviceOutPara;
	}
	public String getAuthCode() {
		return authCode;
	}
	public void setAuthCode(String authCode) {
		this.authCode = authCode;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getFuncName() {
		return funcName;
	}
	public void setFuncName(String funcName) {
		this.funcName = funcName;
	}
	
	
	
	
	
}
